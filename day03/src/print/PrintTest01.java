package print;
// 7번 : 출력 메서드 (println, print)

public class PrintTest01 {
	public static void main(String[] args) {
		System.out.println("오늘은 3일차 수업입니다.");
		System.out.println("4교시 중입니다.");
		System.out.print("프린트로 출력 메서드 연습 중입니다.\n");
		System.out.print("출력 메서드 println()은 소괄호의 내용을 출력하고 줄바꿈되고\n"
				+ "print()는 소괄호 안의 내용을 출력하고 줄바꿈되지 않습니다.");
	}	
}
